<p><span class="bank">6、短线高手</span> </p>
<p>为满足部分投资者对简单操作的需求，我们特推出短线高手指标。它主要通过多空线和买卖提示点给与投资者操作意见。在操作时当紫色线上穿白色线时并伴随有“买’字样提示和红色向上箭头时为买入提示点，当紫色线下穿白色线并伴随着绿色卖出点和向下的箭头时为提示卖出点。依据此指标操作，可使投资简单更加简单明了。</p>
