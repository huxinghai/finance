 <p><span class="bank">5、主力追踪</span> </p>
<p>在股市中追踪主力的动向可以帮助我们把握操作方向，主力追踪指标帮助投资者抓住主力做盘操作意图以及资金进场和离场的信号。在操作时，当出现紫色区域时为主力控盘并且在伴随着红色资金流入图的增高而一路持有，当红色资金流入图逐渐缩短并逐渐转换为蓝色资金流出图时为最后出局点，当进入绿色无庄空时区域时则观望不操作等待下一次机会的出现。</p>
