<p>为了让不同类型的投资者都能找到符合自己的操作模式的指标，我们金股出现指标可以满足不同投资策略的投资者，无论你是激进派、稳健派,只要正确使用本指标，你将无往而不利。操作中，当紫色线上穿黄色线并且伴随着紫色的操作提示区间时为稳健派买入点当紫色线逐渐上升出现黄色区域时为进入顶部区域，当出现绿色柱状图时为出局信号，为稳健派卖出点。激进派在遇到紫色区域出现时就可以买入，在紫色线下穿时卖出。</p>
